    {
        $where = [];
        if($request->only('commodity_id')){
            $name = $request->input('commodity_id');
            $where['commodity_id'] = $name;
        }
        $list = Buyers_orders::where('commodity_id','like','%'.$name.'%')->paginate(2);
        return view("Admin.buyers_orders.index",["list"=>$list,'where'=>$where]);
    }

